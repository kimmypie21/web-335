def add(firstNumber, secondNumber):
    return firstNumber + secondNumber

def subtract(firstNumber, secondNumber):
    return firstNumber - secondNumber

def divide(firstNumber, secondNumber):
    return firstNumber / secondNumber

print(add(10, 45))
print(subtract(45, 10))
print(divide(45, 10))
